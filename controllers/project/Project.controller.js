      dueDate: dueDate,
      refs: refs,
      billNo: billNo,
      remark: remark,
      customer: customer || {
        _id: "",
        name: "",
        customerType: "",
        customerTel: "",
      },
      status: status,
      permisses: permiss,
      employees: employees,
      sendAddress: sendAddress,
